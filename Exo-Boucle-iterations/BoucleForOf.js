// Boucle For-Of - Algorithme d'affichage des éléments du Tableau "tab" ligne par ligne

let tab = ['Js', 'Solidity', 'Html', 'Css', 'Python', 'Java', 'C++', 'Rust']    // tableau d'éléments

for (const elem of tab) {                 // Boucle For sur chaque éléments de "tab"
  console.log(elem)                       // Affichage des résultats des éléments ligne par ligne car situé dans la boucle For
}