// Convertion des valeurs de Décimal en Binaire, Octal et Hexadéciimal via la command .toString()
// Rappel:
// binaire: 2 digits, chiffre 0 et 1 uniquement
// octal: 8 digits, chiffre de 0 à 7 uniquement
// décimal: 10 digits, chiffre de 0 à 9 uniquement
// hexadecimal: 16 digits, chiffre de 0 à 9 et lettres de A à F
console.log(`La variable Décimal ${a} donne en Binaire: ${a.toString(2)} en Octal: ${a.toString(8)} et en Hexadecimal: ${a.toString(8)}`)
